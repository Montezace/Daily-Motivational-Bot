application = Application.builder().token(TELEGRAM_API_TOKEN).build()

# This calls the 'send_quote' function for any text instead or just /start or /qoute
application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, send_quote))
application.add_handler(CommandHandler("id", get_id))

#This schedules the times the quotes are sent to me
application.job_queue.run_daily(daily_quote_job, time=time(hour=6, minute=0, tzinfo=timezone(timedelta(hours=2))))
#start the bot
