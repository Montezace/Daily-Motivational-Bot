async def send_quote(update, context):
    """This function handles any incoming message and sends a quote."""
    # Selecting a random quote
    random_quote = random.choice(list(quotes.values()))
    # Reply to user
    await update.message.reply_text(f"{random_quote}")
#Sending the quotes to me daily
async def daily_quote_job(context: ContextTypes.DEFAULT_TYPE):
    quote = random.choice(list(quotes.values()))
    await context.bot.send_message(chat_id=944823578 , text=quote)

#For me to check my Telegram ID 
async def get_id(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(f"It is: {update.effective_user.id}")

# MAIN PART

